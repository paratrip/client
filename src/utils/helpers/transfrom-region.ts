export const transformRegion = (region: string) => {
  switch (region) {
    case 'YI':
      return '용인';
    case 'PC':
      return '평창';
    case 'BR':
      return '보령';
    case 'DY':
      return '단양';
    case 'GOKS':
      return '곡성';
    case 'YY':
      return '영월';
    case 'GUNS':
      return '군산';
    case 'HC':
      return '합천';
    case 'NH':
      return '남해';
    case 'YS':
      return '여수';
  }
};
